static uint64_t read_u64(FILE *f) {
    uint64_t v;
    if (fread(&v, 8, 1, f) != 1) return 0;
    return v;
}

static uint32_t read_u32(FILE *f) {
    uint32_t v;
    if (fread(&v, 4, 1, f) != 1) return 0;
    return v;
}

static int32_t read_i32(FILE *f) {
    int32_t v;
    if (fread(&v, 4, 1, f) != 1) return 0;
    return v;
}

static int64_t read_i64(FILE *f) {
    int64_t v;
    if (fread(&v, 8, 1, f) != 1) return 0;
    return v;
}

static float read_f32(FILE *f) {
    float v;
    if (fread(&v, 4, 1, f) != 1) return 0;
    return v;
}

static void read_str(FILE *f, char *buf, int max_len) {
    uint64_t len = read_u64(f);
    if (len >= (uint64_t)max_len) len = max_len - 1;
    size_t n = fread(buf, 1, len, f);
    buf[n] = '\0';
    // Skip remaining if truncated
    if (len > (uint64_t)n) fseek(f, len - n, SEEK_CUR);
}

// Float16 → Float32
static float f16_to_f32(uint16_t h) {
    uint32_t sign = (h >> 15) & 1;
    uint32_t exp  = (h >> 10) & 0x1F;
    uint32_t mant = h & 0x03FF;
    if (exp == 0) {
        // Subnormal: value = (-1)^sign * mant/1024 * 2^(-14)
        uint32_t normal_f32 = (sign << 31) | ((1 + 112) << 23) | (mant << 13);
        float normal_val;
        memcpy(&normal_val, &normal_f32, 4);
        if (sign) {
            return normal_val + 6.103515625e-5f;  // 2^(-14), adds because normal_val is negative
        } else {
            return normal_val - 6.103515625e-5f;  // 2^(-14)
        }
    }
    if (exp == 31) {
        // Inf or NaN: propagate to float32
        // FP16: exp=31, mant=0 → Inf, mant!=0 → NaN
        // FP32: exp=255, mant=0 → Inf, mant!=0 → NaN (mant shifted << 13)
        uint32_t f32 = (sign << 31) | (0xFF << 23) | (mant << 13);
        float result;
        memcpy(&result, &f32, 4);
        return result;
    }
    uint32_t f32 = (sign << 31) | ((exp + 112) << 23) | (mant << 13);
    float result;
    memcpy(&result, &f32, 4);
    return result;
}

gguf_ctx* gguf_open(const char *path) {
    gguf_ctx *ctx = calloc(1, sizeof(gguf_ctx));
    if (!ctx) return NULL;
    
    FILE *f = fopen(path, "rb");
    if (!f) { free(ctx); return NULL; }
    ctx->file = f;
    
    // Magic
    char magic[4];
    if (fread(magic, 1, 4, f) != 4 || memcmp(magic, "GGUF", 4) != 0) {
        fclose(f); free(ctx); return NULL;
    }
    
    ctx->version = read_u32(f);
    ctx->n_tensors = read_i64(f);
    ctx->n_kv = read_i64(f);
    ctx->alignment = 32; // default
    
    fprintf(stderr, "GGUF v%u, %ld tensors, %ld KV pairs\n", 
            ctx->version, ctx->n_tensors, ctx->n_kv);
    
    // Skip KV pairs
    for (int64_t i = 0; i < ctx->n_kv; i++) {
        uint64_t key_len = read_u64(f);
        fseek(f, key_len, SEEK_CUR);
        
        int32_t typ = read_i32(f);
        
        switch (typ) {
            case 8: { // string
                uint64_t vlen = read_u64(f);
                fseek(f, vlen, SEEK_CUR);
                break;
            }
            case 4: case 5: case 6: fseek(f, 4, SEEK_CUR); break; // u32/i32/f32
            case 7: fseek(f, 1, SEEK_CUR); break; // bool
            case 10: case 11: fseek(f, 8, SEEK_CUR); break; // u64/i64
            case 9: { // array
                int32_t arr_type = read_i32(f);
                uint64_t arr_len = read_u64(f);
                if (arr_type == 8) {
                    for (uint64_t j = 0; j < arr_len; j++) {
                        uint64_t slen = read_u64(f);
                        fseek(f, slen, SEEK_CUR);
                    }
                } else {
                    int elem_size = 4;
                    if (arr_type == 0 || arr_type == 1 || arr_type == 7) elem_size = 1;
                    else if (arr_type == 2 || arr_type == 3) elem_size = 2;
                    else if (arr_type == 10 || arr_type == 11 || arr_type == 12) elem_size = 8;
                    fseek(f, arr_len * elem_size, SEEK_CUR);
                }
                break;
            }
            default: fseek(f, 4, SEEK_CUR); break;
        }
    }
    
    // Read tensor info
    ctx->tensors = calloc(ctx->n_tensors, sizeof(gguf_tensor_info));
    for (int64_t i = 0; i < ctx->n_tensors; i++) {
        read_str(f, ctx->tensors[i].name, sizeof(ctx->tensors[i].name));
        ctx->tensors[i].n_dims = read_u32(f);
        for (int d = 0; d < ctx->tensors[i].n_dims && d < 4; d++) {
            ctx->tensors[i].dims[d] = read_i64(f);
        }
        ctx->tensors[i].ggml_type = read_i32(f);
        ctx->tensors[i].data_offset = read_u64(f);
    }
    
    // Data blob start
    long data_start = ftell(f);
    long pad = (ctx->alignment - (data_start % ctx->alignment)) % ctx->alignment;
    ctx->data_blob_offset = data_start + pad;
    
    fprintf(stderr, "Tensor info end at offset %ld, aligned to %lu (pad=%ld, alignment=%ld)\n", 
            data_start, ctx->data_blob_offset, pad, ctx->alignment);
    
    return ctx;
}

gguf_tensor_info* gguf_find_tensor(gguf_ctx *ctx, const char *name) {
    for (int64_t i = 0; i < ctx->n_tensors; i++) {
        if (strcmp(ctx->tensors[i].name, name) == 0) {
            return &ctx->tensors[i];
        }
    }
    return NULL;
}

// ========== Q5_K Dequantization ==========

#define QK_K 256
// block_q4_K: d(2) + dmin(2) + scales(12) + qs(128) = 144 bytes (NO qh field in modern format)
#define Q4_K_BLOCK_SIZE 144
// block_q5_K: d(2) + dmin(2) + scales(12) + qh(32) + qs(128) = 176 bytes
#define Q5_K_BLOCK_SIZE 176

// Forward declaration for 6-bit scale extraction (defined after Q5_K)
static inline void get_scale_min_k4(int j, const uint8_t *q, uint8_t *d, uint8_t *m);

static void dequantize_q5_K_row(const uint8_t *data, float *output, int64_t n_elems) {
    // Reference: llama.cpp ggml-quants.c dequantize_row_q5_K()
    // block_q5_K: d(2) + dmin(2) + scales(12) + qh(32) + qs(128) = 176 bytes
    // scales use 6-bit encoding via get_scale_min_k4 (same as Q4_K)
    // qh provides 256 high bits packed linearly: bit i = value for element i
    // qs stores 256 low 4-bit nibbles
    int64_t n_blocks = (n_elems + QK_K - 1) / QK_K;
    
    for (int64_t b = 0; b < n_blocks; b++) {
        const uint8_t *block = data + b * Q5_K_BLOCK_SIZE;
        
        // Extract d and dmin (float16 at bytes 0-3)
        uint16_t d_bits, dmin_bits;
        memcpy(&d_bits, block, 2);
        memcpy(&dmin_bits, block + 2, 2);
        float d = f16_to_f32(d_bits);
        float dmin = f16_to_f32(dmin_bits);
        
        const uint8_t *scales = block + 4;  // 12 bytes — 6-bit scales (get_scale_min_k4)
        const uint8_t *qh = block + 16;     // 32 bytes — 256 high bits, 1 per element
        const uint8_t *qs = block + 48;     // 128 bytes — low 4-bit nibbles
        
        int64_t out_offset = b * QK_K;
        int64_t remaining = n_elems - out_offset;
        if (remaining > QK_K) remaining = QK_K;
        
        int is = 0;
        uint8_t sc, m;
        
        // 4 groups of 64 elements each
        for (int j = 0; j < QK_K && j < remaining; j += 64) {
            // Get 6-bit scale/min packed in scales (same as Q4_K)
            get_scale_min_k4(is + 0, scales, &sc, &m);
            float d1 = d * sc; float m1 = dmin * m;
            get_scale_min_k4(is + 1, scales, &sc, &m);
            float d2 = d * sc; float m2 = dmin * m;
            
            int ql_base = j / 2; // 32 qs bytes per 64-element group
            // qh stores high bits for each 32-element half-group:
            // Each qh byte = 4 pairs, one pair per 64-element chunk
            // bit [chunk*2+0] = val1 high bit (elements chunk*64 .. chunk*64+31)
            // bit [chunk*2+1] = val2 high bit (elements chunk*64+32 .. chunk*64+63)
            int chunk_id = j / 64;  // 0, 1, 2, 3
            
            for (int l = 0; l < 32 && (j + l) < remaining; l++) {
                uint8_t lo = qs[ql_base + l];
                
                // High bit for val1 element: qh[l] bit [chunk_id*2+0]
                int hi1 = (qh[l] >> (chunk_id * 2 + 0)) & 1;
                // High bit for val2 element: qh[l] bit [chunk_id*2+1]
                int hi2 = (qh[l] >> (chunk_id * 2 + 1)) & 1;
                
                float val1 = (float)((lo & 0x0F) + (hi1 ? 16 : 0));
                float val2 = (float)((lo >> 4)   + (hi2 ? 16 : 0));
                
                int idx1 = out_offset + j + l;
                int idx2 = out_offset + j + 32 + l;
                
                if (idx1 < n_elems) output[idx1] = d1 * val1 - m1;
                if (idx2 < n_elems) output[idx2] = d2 * val2 - m2;
            }
            
            is += 2;
        }
    }
}

// ========== IQ4_XS Dequantization (4.25 bpw, reference llama.cpp) ==========
// block_iq4_xs: d(2) + scales_h(2) + scales_l[4] + qs[128] = 136 bytes

static const int8_t kvalues_iq4nl[16] = {
    -127, -104, -83, -65, -49, -35, -22, -10,
      1,   13,   25,  38,  53,  69,  89, 113
};

void dequantize_iq4_xs_row(const uint8_t *data, float *output, int64_t n_elems) {
    // Reference: llama.cpp ggml-quants.c dequantize_row_iq4_xs()
    // block layout: d(fp16,2) + scales_h(uint16,2) + scales_l[4] + qs[128] = 136 bytes
    int64_t n_blocks = (n_elems + QK_K - 1) / QK_K;
    for (int64_t b = 0; b < n_blocks; b++) {
        const uint8_t *block = data + b * 136;
        uint16_t d_bits;
        memcpy(&d_bits, block, 2);
        float d = f16_to_f32(d_bits);
        
        uint16_t scales_h;
        memcpy(&scales_h, block + 2, 2);
        const uint8_t *scales_l = block + 4;
        const uint8_t *qs = block + 8;  // qs starts after d+scales_h+scales_l
        
        float *y = output + b * QK_K;
        for (int ib = 0; ib < QK_K/32; ib++) {
            int ls = ((scales_l[ib/2] >> 4*(ib%2)) & 0xf) | (((scales_h >> 2*ib) & 3) << 4);
            float dl = d * (ls - 32);
            for (int j = 0; j < 16; j++) {
                y[j+ 0] = dl * kvalues_iq4nl[qs[j] & 0xf];
                y[j+16] = dl * kvalues_iq4nl[qs[j] >>  4];
            }
            y  += 32;
            qs += 16;
        }
    }
}

#define Q6_K_BLOCK_SIZE 210  // sizeof(block_q6_K): 128+64+16+2

// IQ2 block sizes (used in gguf_read_tensor_f32) — verified from GGUF data offsets
// block_iq2_xxs: 66 bytes per 256 elements = 2.0625 bpw
#define IQ2_XXS_BLOCK_SIZE 66
#define IQ2_S_BLOCK_SIZE 82
#define IQ3_XXS_BLOCK_SIZE 98  // d(fp16,2) + qs[96] = 98 bytes
#define IQ3_S_BLOCK_SIZE 110   // d(2) + qs[64] + qh[8] + signs[32] + scales[4] = 110
#define IQ1_M_BLOCK_SIZE 56    // qs[32] + qh[16] + scales[8] = 56

// Forward declarations for dequant functions defined after gguf_read_tensor_f32
void dequantize_iq3_xxs_row(const uint8_t *data, float *output, int64_t n_elems);
void dequantize_iq3_s_row(const uint8_t *data, float *output, int64_t n_elems);
void dequantize_iq1_m_row(const uint8_t *data, float *output, int64_t n_elems);

void dequantize_q6_K_row(const uint8_t *data, float *output, int64_t n_elems) {
    int64_t n_blocks = (n_elems + QK_K - 1) / QK_K;
    
    for (int64_t b = 0; b < n_blocks; b++) {
        const uint8_t *block = data + b * Q6_K_BLOCK_SIZE;
        
        // Extract d (float16 at the end of the block)
        uint16_t d_bits;
        memcpy(&d_bits, block + 208, 2);  // d is at offset 208 (128+64+16)
        float d = f16_to_f32(d_bits);
        
        const uint8_t *ql = block;         // 128 bytes: low 4 bits of quant
        const uint8_t *qh = block + 128;   // 64 bytes: high 2 bits
        const int8_t *sc = (const int8_t *)(block + 192);  // 16 bytes: scales
        
        for (int n = 0; n < QK_K; n += 128) {
            for (int l = 0; l < 32; ++l) {
                int is = l/16;
                int8_t q1 = (int8_t)((ql[l +  0] & 0xF) | (((qh[l] >> 0) & 3) << 4)) - 32;
                int8_t q2 = (int8_t)((ql[l + 32] & 0xF) | (((qh[l] >> 2) & 3) << 4)) - 32;
                int8_t q3 = (int8_t)((ql[l +  0] >> 4)   | (((qh[l] >> 4) & 3) << 4)) - 32;
                int8_t q4 = (int8_t)((ql[l + 32] >> 4)   | (((qh[l] >> 6) & 3) << 4)) - 32;
                
                int64_t base = b * QK_K + n;
                output[base + l +  0] = d * sc[is + 0] * q1;
                output[base + l + 32] = d * sc[is + 2] * q2;
                output[base + l + 64] = d * sc[is + 4] * q3;
                output[base + l + 96] = d * sc[is + 6] * q4;
            }
            ql += 64; qh += 32; sc += 8;
        }
    }
}

// ========== Tensor Reading ==========

// Forward declaration for Q4_K dequant function
static void dequantize_q4_K_row(const uint8_t *data, float *output, int64_t n_elems);

int gguf_read_tensor_f32(gguf_ctx *ctx, gguf_tensor_info *tensor, float *output, int64_t max_elems) {
    // Calculate total elements
    int64_t n_elems = 1;
    for (int d = 0; d < tensor->n_dims; d++) n_elems *= tensor->dims[d];
    
    if (n_elems > max_elems) {
        fprintf(stderr, "Error: tensor too large (%ld elems, max %ld)\n", n_elems, max_elems);
        return 0;
    }
    
    // Get raw data pointer (from buffer or file)
    uint64_t tensor_pos = ctx->data_blob_offset + tensor->data_offset;
    const uint8_t *src = NULL;
    uint8_t *raw_heap = NULL;
    if (ctx->data_blob) {
        src = (const uint8_t *)ctx->data_blob + tensor->data_offset;
    }
    
    if (tensor->ggml_type == GGML_TYPE_F32) {
        // Direct read
        if (src) {
            memcpy(output, src, n_elems * sizeof(float));
        } else {
            fseek(ctx->file, tensor_pos, SEEK_SET);
            size_t n_read = fread(output, 4, n_elems, ctx->file);
            if (n_read != (size_t)n_elems) return 0;
        }
        return (int)n_elems;
    }
    else if (tensor->ggml_type == GGML_TYPE_F16) {
        // Half-float to float
        if (src) {
            for (int64_t i = 0; i < n_elems; i++) {
                uint16_t h;
                memcpy(&h, src + i * 2, 2);
                output[i] = f16_to_f32(h);
            }
        } else {
            fseek(ctx->file, tensor_pos, SEEK_SET);
            for (int64_t i = 0; i < n_elems; i++) {
                uint16_t h;
                if (fread(&h, 2, 1, ctx->file) != 1) return (int)i;
                output[i] = f16_to_f32(h);
            }
        }
        return (int)n_elems;
    }
    
    // For quantized types: ensure we have raw data pointer (from data_blob or file read)
    if (!src) {
        // Calculate raw size and read into heap buffer
        int64_t raw_size = gguf_raw_size(tensor->ggml_type, n_elems);
        if (raw_size <= 0) {
            fprintf(stderr, "Error: unknown type %d (cannot determine raw size)\n", tensor->ggml_type);
            return 0;
        }
        raw_heap = (uint8_t *)malloc(raw_size);
        if (!raw_heap) return 0;
        fseek(ctx->file, tensor_pos, SEEK_SET);
        size_t n_read = fread(raw_heap, 1, raw_size, ctx->file);
        if (n_read != (size_t)raw_size) {
            free(raw_heap);
            return 0;
        }
        src = raw_heap;
    }
    
    // Dequantize based on type (runs regardless of src origin)
    if (tensor->ggml_type == GGML_TYPE_Q6_K) {
        // Q6_K dequantization (6.5625 bpw)
        int64_t n_blocks = (n_elems + QK_K - 1) / QK_K;
        dequantize_q6_K_row(src, output, n_elems);
    }
    else if (tensor->ggml_type == GGML_TYPE_Q5_K) {
        dequantize_q5_K_row(src, output, n_elems);
    }
    else if (tensor->ggml_type == GGML_TYPE_Q8_0) {
        // Q8_0 dequantization (8.5 bpw, 32 elements per block)
        int64_t q8_blocks = (n_elems + 31) / 32;
        for (int64_t b = 0; b < q8_blocks; b++) {
            uint16_t d_bits;
            memcpy(&d_bits, src + b * 34, 2);
            float d = f16_to_f32(d_bits);
            const int8_t *qs = (const int8_t *)(src + b * 34 + 2);
            for (int j = 0; j < 32 && b * 32 + j < n_elems; j++)
                output[b * 32 + j] = d * (float)qs[j];
        }
    }
    else if (tensor->ggml_type == GGML_TYPE_IQ1_S) {
        dequantize_iq1_s_row(src, output, n_elems);
    }
    else if (tensor->ggml_type == GGML_TYPE_Q4_K) {
        dequantize_q4_K_row(src, output, n_elems);
    }
    else if (tensor->ggml_type == GGML_TYPE_IQ2_XXS) {
        dequantize_iq2_xxs_row(src, output, n_elems);
    }
    else if (tensor->ggml_type == GGML_TYPE_IQ2_S) {
        dequantize_iq2_s_row(src, output, n_elems);
    }
    else if (tensor->ggml_type == GGML_TYPE_IQ3_S) {
        dequantize_iq3_s_row(src, output, n_elems);
    }
    else if (tensor->ggml_type == GGML_TYPE_IQ3_XXS) {
        dequantize_iq3_xxs_row(src, output, n_elems);
    }
    else if (tensor->ggml_type == GGML_TYPE_IQ1_M) {
        dequantize_iq1_m_row(src, output, n_elems);
    }
    else {
        fprintf(stderr, "Error: unsupported GGML type %d for %s\n", tensor->ggml_type, tensor->name);
        if (raw_heap) free(raw_heap);
        return 0;
    }
    
    // Cleanup
    if (raw_heap) free(raw_heap);
    return (int)n_elems;
}

// Read a float32 KV pair from the GGUF file by re-opening it (independent of ctx->file)
float gguf_read_kv_f32(const char *path, const char *key, float default_val) {
    FILE *f = fopen(path, "rb");
    if (!f) return default_val;
    
    // GGUF header after magic(4): version(4) + n_tensors(8) + n_kv(8)
    fseek(f, 4, SEEK_SET);
    uint32_t version = read_u32(f); (void)version;
    int64_t n_tensors = read_i64(f); (void)n_tensors;
    int64_t n_kv = read_i64(f);
    
    for (int64_t i = 0; i < n_kv; i++) {
        uint64_t key_len = read_u64(f);
        char kbuf[256];
        size_t read_len = key_len < 255 ? key_len : 255;
        if (fread(kbuf, 1, read_len, f) != read_len) { fclose(f); return default_val; }
        kbuf[read_len] = '\0';
        if (read_len < key_len) fseek(f, key_len - read_len, SEEK_CUR);
        
        int32_t typ = read_i32(f);
        
        if (strcmp(kbuf, key) == 0) {
            if (typ == 6) { // f32
                float val;
                if (fread(&val, sizeof(float), 1, f) == 1) { fclose(f); return val; }
            }
            fclose(f);
            return default_val;
        } else {
            // Skip value based on type
            switch (typ) {
                case 0: { uint8_t v; fread(&v,1,1,f); break; }
                case 1: { int8_t v; fread(&v,1,1,f); break; }
                case 2: { uint16_t v; fread(&v,2,1,f); break; }
                case 3: { int16_t v; fread(&v,2,1,f); break; }
                case 4: { uint32_t v; fread(&v,4,1,f); break; }
                case 5: { int32_t v; fread(&v,4,1,f); break; }
                case 6: { float v; fread(&v,4,1,f); break; }
                case 7: { uint64_t v; fread(&v,8,1,f); break; }
                case 8: { int64_t v; fread(&v,8,1,f); break; }
                case 9: { double v; fread(&v,8,1,f); break; }
                case 10: { // bool
                    uint8_t v;
                    fread(&v,1,1,f);
                    break;
                }
                default: {
                    uint64_t arr_len = read_u64(f);
                    int32_t arr_typ = read_i32(f);
                    for (uint64_t a = 0; a < arr_len; a++) {
                        uint8_t discard[8];
                        fread(discard, 1, 8, f);
                    }
                    break;
                }
            }
        }
    }
    
    fclose(f);
    return default_val;
}


/**
 * IQ2_XXS block-level operations for on-the-fly dequant dot product.
 *
 * The block format matches llama.cpp's block_iq2_xxs:
 *   d (fp16, 2 bytes) + qs (uint16_t[32], 64 bytes) = 66 bytes per 256 elements
 *
 * For each group of 32 elements (ib32 = 0..7):
 *   aux32[0] = grid indices for 4 sub-groups (1 byte each => 4 bytes)
 *   aux32[1] = sign info packed in bits 7*l (7 bits each) + scale in top 4 bits
 *   aux8[l] = grid index for sub-group l (0..255)
 *   signs = ksigns_iq2xs[(aux32[1] >> (7*l)) & 0x7F]
 *   db = d * (0.5f + (aux32[1] >> 28)) * 0.25f
 *   For j=0..7: output[8*l+j] = db * grid[aux8[l]][j] * (signs bit j ? -1 : +1)
 */

#include <stdint.h>
#include <string.h>

// IQ2 block constants
#define IQ2_XXS_BLOCK_SIZE 66
#define QK_K 256

// Float16 → Float32 (local copy so we don't depend on gguf_reader.c internals)
static float f16_to_f32_local(uint16_t h) {
    uint32_t sign = (h >> 15) & 1;
    uint32_t exp  = (h >> 10) & 0x1F;
    uint32_t mant = h & 0x03FF;
    uint32_t f32;
    if (exp == 0) {
        f32 = (sign << 31) | ((uint32_t)(127 - 15 + 1) << 23) | (mant << 13);
    } else if (exp == 31) {
        f32 = (sign << 31) | (0xFF << 23) | (mant << 13);
    } else {
        f32 = (sign << 31) | ((uint32_t)(127 - 15 + exp) << 23) | (mant << 13);
    }
    float result;
    memcpy(&result, &f32, 4);
    return result;
}


// Sign bit masks
static const uint8_t kmask_iq2xs[8] = {1, 2, 4, 8, 16, 32, 64, 128};

// ============================================================
// Block-level: dequantize one IQ2_XXS block (66 bytes -> 256 floats)
// ============================================================

void dequantize_iq2_xxs_block(const uint8_t *block, float *output) {
    // d is fp16 at bytes 0-1
    uint16_t d_bits;
    memcpy(&d_bits, block, 2);
    float d = f16_to_f32_local(d_bits);
    // qs is at bytes 2..65 (64 bytes interpreted as 32 uint16_t)
    const uint16_t *qs16 = (const uint16_t *)(block + 2);

    uint32_t aux32[2];
    const uint8_t *aux8 = (const uint8_t *)aux32;

    for (int ib32 = 0; ib32 < QK_K/32; ib32++) {
        // Read 8 bytes: 2 uint32_t
        memcpy(aux32, qs16 + 4*ib32, 2*sizeof(uint32_t));
        // db = d * (0.5f + scale_factor) * 0.25f, where scale_factor = aux32[1] >> 28
        float db = d * (0.5f + (float)(aux32[1] >> 28)) * 0.25f;
        for (int l = 0; l < 4; l++) {
            // Grid index from aux8[l] (1 byte), lookup in iq2xxs_grid[256]
            const uint8_t *grid = (const uint8_t *)(&iq2xxs_grid[aux8[l]]);
            // Sign bits: ksigns_iq2xs[(aux32[1] >> (7*l)) & 127]
            uint8_t signs = ksigns_iq2xs[(aux32[1] >> (7*l)) & 127];
            int base = ib32 * 32 + l * 8;
            for (int j = 0; j < 8; j++) {
                float val = db * (float)grid[j];
                if (signs & kmask_iq2xs[j]) {
                    output[base + j] = -val;
                } else {
                    output[base + j] = val;
                }
            }
        }
    }
}

// ============================================================
// Fused: dequantize one IQ2_XXS block AND compute dot product
// with input vector x[base:base+256]
// ============================================================

float iq2_xxs_dot_block(const uint8_t *block, const float *x) {
    // d is fp16 at bytes 0-1
    uint16_t d_bits;
    memcpy(&d_bits, block, 2);
    float d = f16_to_f32_local(d_bits);
    const uint16_t *qs16 = (const uint16_t *)(block + 2);

    uint32_t aux32[2];
    const uint8_t *aux8 = (const uint8_t *)aux32;

    float total = 0.0f;

    for (int ib32 = 0; ib32 < QK_K/32; ib32++) {
        memcpy(aux32, qs16 + 4*ib32, 2*sizeof(uint32_t));
        float db = d * (0.5f + (float)(aux32[1] >> 28)) * 0.25f;
        for (int l = 0; l < 4; l++) {
            const uint8_t *grid = (const uint8_t *)(&iq2xxs_grid[aux8[l]]);
            uint8_t signs = ksigns_iq2xs[(aux32[1] >> (7*l)) & 127];
            int base = ib32 * 32 + l * 8;
            for (int j = 0; j < 8; j++) {
                float val = db * (float)grid[j];
                if (signs & kmask_iq2xs[j]) {
                    total += -val * x[base + j];
                } else {
                    total += val * x[base + j];
                }
            }
        }
    }
    return total;
}

// ============================================================
// Dequantize one row (multiple blocks) and compute dot product
// with input vector x. n_elems must be a multiple of 256.
// ============================================================

float iq2_xxs_dot_row(const uint8_t *qdata, int n_elems, const float *x) {
    int n_blocks = n_elems / 256;
    float total = 0.0f;
    for (int b = 0; b < n_blocks; b++) {
        total += iq2_xxs_dot_block(qdata + b * 66, x + b * 256);
    }
    return total;
}
